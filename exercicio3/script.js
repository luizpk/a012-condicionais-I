let nacionalidade = prompt("Escreva aqui sua nacionalidade.").toUpperCase()
if(nacionalidade===("BRASILEIRA")){
    alert(`Que legal, você é brasileiro!`)
}else if(nacionalidade===("ARGENTINA")){
    alert(`Que legal, você é argentino!`)
}else if(nacionalidade===("URUGUAIA")){
    alert(`Que legal, você é uruguaio!`)
}else if(nacionalidade===("CHILENA")){
    alert(`Que legal, você é chileno!`)
}else if(nacionalidade===("COLOMBIANA")){
    alert(`Que legal, você é colombiano!`)
}else {
    alert("Nacionalidade não encontrada")
}
